package World;

public abstract class EssereVivente {
	
	int age;
	String name;
	
	
	
	
	public EssereVivente(int age, String name) {
		this.age = age;
		this.name = name;
	}
	
	

	public abstract String mangia();
	
	public void descrizione(){
		System.out.println("per vivere deve mangiare "+ mangia() );
		
	}
	
	

}
