package it.polito.oop.vaccination;

public class CapacitaVaccinazione {
	CentroVaccinale centro;
	Giorno giorno;
	int capacita;
	public CapacitaVaccinazione(CentroVaccinale centro, Giorno giorno, int cap) {
		super();
		this.centro = centro;
		this.giorno = giorno;
		this.capacita=cap;
	}
	public CentroVaccinale getCentro() {
		return centro;
	}
	public void setCentro(CentroVaccinale centro) {
		this.centro = centro;
	}
	public Giorno getGiorno() {
		return giorno;
	}
	public void setGiorno(Giorno giorno) {
		this.giorno = giorno;
	}
	public int getCapacita() {
		return capacita;
	}
	public void setCapacita(int capacita) {
		this.capacita = capacita;
	}
	
	

}
