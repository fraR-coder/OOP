package it.polito.oop.vaccination;

public class Persona {
	String codF,nome,cognome;
	int year;
	int eta;
	boolean allocato;
	public Persona(String codF, String nome, String cognome, int year) {
		this.codF = codF;
		this.nome = nome;
		this.cognome = cognome;
		this.year = year;
		
	}
	public String getCodF() {
		return codF;
	}
	public void setCodF(String codF) {
		this.codF = codF;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getEta() {
		return eta;
	}
	public void setEta(int eta) {
		this.eta = eta;
	}
	public boolean isAllocato() {
		return allocato;
	}
	public void setAllocato(boolean allocato) {
		this.allocato = allocato;
	}
	
	
	

}
